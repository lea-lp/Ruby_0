# .. #{} - englobe la ou les valeur(s) pour qu'elle(s) effectuent une opération entre elle(s),
# idem quand opération sans "" après 'puts'

# .. puts - affiche une string sur la console


puts "On va compter le nombre d'heures de travail à THP"
# affiche "texte" (problème a résoudre)

puts "Travail : #{10 * 5 * 11}"
# "texte + #{multiplication = résultat sur console}" (calcul = réponse)

puts "En minutes ça fait : #{10 * 5 * 11 * 60}"
# idem commentaire précédent

puts "Et en secondes ?"
# "texte" (problème)

puts 10 * 5 * 11 * 60 * 60
# affiche résultat (calcul = réponse)
# si pas ""; calcul de la ligne; réponse = true or false


puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
# "texte" (problème)

puts 3 + 2 < 5 - 7
# affiche résultat (5 < -2 = réponse: false)

puts "Ça fait combien 3 + 2 ? #{3 + 2}"
# "texte (problème) + #{multiplication = résultat sur console}" (calcul = réponse)

puts "Ça fait combien 5 - 7 ? #{5 - 7}"
# idem string précédente

puts "Ok, c'est faux alors !"
# "texte"

puts "C'est drôle ça, faisons-en plus :"
# "texte" (problème)

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
# "texte (problème) + #{5 > -2 = résultat sur console}" (calcul = réponse: true)
# réponse = true or false

puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
# idem + #{5 >= -2 = réponse: true}"

puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"
# idem + #{5 <= -2 = réponse: false}"
