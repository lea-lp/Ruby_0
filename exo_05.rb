# Que fait #{} ? - englobe la ou les valeur•s 
# pour qu'elle•s effectue•nt une opération •entre elles
# idem quand opération sans "" après 'puts'

# puts - affiche une string ou un résultat sur la console


puts "On va compter le nombre d'heures de travail à THP"
# affiche "string"
# (problème a résoudre)

puts "Travail : #{10 * 5 * 11}"
# "string + #{multiplication = résultat sur console}"
# (calcul = réponse)

puts "En minutes ça fait : #{10 * 5 * 11 * 60}"
# idem précédent

puts "Et en secondes ?"
# "string"
# (problème)

puts 10 * 5 * 11 * 60 * 60
# affiche résultat -entiers-
# (calcul = réponse)
# si pas ""; calcul de la ligne; réponse = true or false

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
# "string" (problème)

puts 3 + 2 < 5 - 7
# affiche résultat -entiers- (
# 5 < -2 = réponse: false)
# variable booléen

puts "Ça fait combien 3 + 2 ? #{3 + 2}"
# "string (problème) + #{multiplication = résultat sur console}"
# (calcul = réponse)

puts "Ça fait combien 5 - 7 ? #{5 - 7}"
# idem précédent

puts "Ok, c'est faux alors !"
# "string"

puts "C'est drôle ça, faisons-en plus :"
# "string"
# (problème)

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
# "string (problème) + #{5 > -2 = résultat sur console}" (
# calcul = réponse: true)
# variable booléen

puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
# idem + #{5 >= -2 = réponse: true}" (booléen)

puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"
# idem + #{5 <= -2 = réponse: false}" (booléen)
